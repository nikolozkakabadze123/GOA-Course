'''3) გააკეთეთ 2-2 მაგალითი ლოგიკურ ოპერატორებზე: and, or, not. კომენტარების საშვლაებით გვერდით მიუწერეთ შედეგი. ახსენით რა არის ლოგიკური ოპერატორი და როგორ მუშაობს ლოგიკური ოპერაცია'''


print(True and True)    # True 
print(False and True)   # False 

print(True or False)    # True
print(False or False)   # False

print(not True)    # False
print(not False)   # True

